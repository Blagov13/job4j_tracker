package ru.job4j.pojo;

public class Student {
    private String lastFirstPatronomic;
    private String group;
    private String date;

    public String getLastFirstPatronomic() {
        return lastFirstPatronomic;
    }

    public void setLastFirstPatronomic(String lastFirstPatronomic) {
        this.lastFirstPatronomic = lastFirstPatronomic;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
